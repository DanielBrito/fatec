public class Lista2_Exe2 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int n = 3;
        int soma = 0;
        
        for(int i=1 ; i<=n ; i++)
        {
            soma = soma+i;
        }
        
        System.out.println("Soma: " + soma);
    }
}
